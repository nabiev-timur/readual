use readual_output::{output, MessageLevel};

// Импортируем макросы для удобного использования
#[macro_use]
extern crate readual_output;

fn main() {
    // Использование макросов с предустановленными уровнями
    success!("Операция выполнена успешно");
    warning!("Это предупреждение о возможной проблеме");
    error!("Произошла критическая ошибка");
    info!("Информационное сообщение");
    debug!("Отладочная информация для разработчика");

    println!();

    // Использование макросов с форматированием
    let file_count = 5;
    success!("Обработано {} файлов", file_count);
    warning!("Файл {} не найден, используется значение по умолчанию", "config.toml");
    error!("Не удалось прочитать файл: {} (код ошибки: {})", "data.txt", 404);
    info!("Начинаю обработку {} документов", 10);
    debug!("Текущее состояние: парсинг заголовков, обработано {} строк", 42);

    println!();

    // Использование макроса output! с явным указанием уровня
    output!(MessageLevel::Success, "Файл {} успешно загружен", "README.md");
    output!(MessageLevel::Warning, "Файл не найден, используется значение по умолчанию");
    output!(MessageLevel::Error, "Не удалось прочитать конфигурацию");
    output!(MessageLevel::Info, "Начинаю обработку документа");
    output!(MessageLevel::Debug, "Текущее состояние: парсинг заголовков");

    println!();

    // Использование макроса output! без уровня (обычный вывод, MessageLevel::None)
    output!("Это обычное сообщение без уровня");
    output!("Обработано {} файлов из {}", 10, 15);
    output!("Текущее время: {:?}", std::time::SystemTime::now());

    println!();

    // Примеры с различными типами данных
    let percentage = 75.5;
    let items = vec!["item1", "item2", "item3"];
    success!("Процесс завершён на {:.1}%", percentage);
    info!("Найдено элементов: {}", items.len());
    debug!("Список элементов: {:?}", items);
}

